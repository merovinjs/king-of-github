import BestStars from "@/components/Beststars/BestStars";
import styles from "./page.module.css";
import { Croissant_One } from "next/font/google";
import { Metadata } from "next";

const croissant = Croissant_One({
  subsets: ["latin-ext"],
  weight: "400",
});

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};
async function getData() {
  const fetchJsstars = await fetch("https://api.github.com/search/repositories?q=language:javascript&sort=stars=desc&per_page=15");
  return fetchJsstars.json();
}

export default async function Home() {
  const data = await getData();
  let first15 = data.items;

  return (
    <main className={styles.main}>
      <h1 className={croissant.className}>King&apos;s of Github</h1>
      <div className={styles.bestStarsContainer}>
        <BestStars first15={first15} />
        <BestStars first15={first15} />
        <BestStars first15={first15} />
        <BestStars first15={first15} />
      </div>
      <h1>footer</h1>
    </main>
  );
}
